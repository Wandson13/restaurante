    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        estoqueDAO pdao = new estoqueDAO();

        for (estoque p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getQuantidade(),
                p.getValor()
            });

        }
    }
