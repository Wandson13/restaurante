    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        prato p = new prato();
        pratoDAO dao = new pratoDAO();
        String combo = (String) jComboBox1.getSelectedItem();
        
        p.setNome(jTextField1.getText());
        p.setPreco(Double.parseDouble(jTextField2.getText()));
        p.setCategoria(combo);
        
        dao.create(p);

        jTextField1.setText("");
        jTextField2.setText("");

        readJTable();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTable1.getSelectedRow() != -1) {

            prato p = new prato();
            pratoDAO dao = new pratoDAO();

            p.setId((int) jTable1.getValueAt(jTable1.getSelectedRow(), 0));      
            dao.delete(p);

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTable1.getSelectedRow() != -1) {

            prato p = new prato();
            pratoDAO dao = new pratoDAO();

            p.setNome(jTextField1.getText());
            p.setPreco(Double.parseDouble(jTextField2.getText()));
            p.setCategoria((String) jComboBox1.getSelectedItem());
            p.setId((int) jTable1.getValueAt(jTable1.getSelectedRow(), 0));
            dao.update(p);

            jTextField1.setText("");
            jTextField2.setText("");

            readJTable();

        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyReleased
        if (jTable1.getSelectedRow() != -1) {
            jTextField1.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString());
            jTextField2.setText(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString());

        }
    }//GEN-LAST:event_jTable1KeyReleased

