    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        pratoDAO pdao = new pratoDAO();
        modelo.setNumRows(0);

        for (prato p : pdao.read()) {
            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getPreco(),
                p.getCategoria(),
            });
        }
    }
    
